import { cookies } from 'next/headers';

export type Locale = 'en' | 'pt';

export async function getLocale(): Promise<Locale> {
  const cookieStore = await cookies();
  const val = cookieStore.get('locale')?.value;
  return val === 'pt' ? 'pt' : 'en';
}

